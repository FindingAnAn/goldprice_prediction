    next_21_day_price_change = EXCLUDED.next_21_day_price_change,
    next_30_day_price_change = EXCLUDED.next_30_day_price_change,
    next_63_day_price_change = EXCLUDED.next_63_day_price_change,
    updated_at = NOW();
